    if [ "$(id -u)" -ne 0 ]; then
        log_error "This script must be run as root"
        exit 1
    fi
    
    if [ -n "${SSH_CONNECTION:-}" ] || [ -n "${SSH_CLIENT:-}" ] || [ -n "${SSH_TTY:-}" ]; then
        echo ""
        log_warn "⚠️  DANGER: This script is being run over SSH!"
        log_warn "This will RESTART SSH and change the port."
        echo -ne "${YELLOW}Type 'FORCE' to proceed anyway:${NC} "
        read -r confirm
        if [[ "$confirm" != "FORCE" ]]; then
            log_info "Setup cancelled"
            exit 0
