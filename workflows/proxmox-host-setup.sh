    log_success "  Proxmox Host Pre-Setup Completed!"
    log_success "═══════════════════════════════════════════"
    echo ""
    
    log_info "Configuration Summary:"
    echo "  • FQDN:         $PVE_HOSTNAME.local"
    echo "  • Admin User:   $NEW_USER"
    echo "  • SSH Port:     $SSH_PORT (Apply after restart)"
    echo "  • Server IP:    $(hostname -I | awk '{print $1}')"
    echo ""
    log_warn "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    log_warn "  ⚠️  SAVE THESE CREDENTIALS NOW"
    log_warn "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo -e "${GREEN}  Admin User: ${NC}${NEW_USER}"
    echo -e "${GREEN}  Password:   ${NC}${GENERATED_USER_PASSWORD}"
