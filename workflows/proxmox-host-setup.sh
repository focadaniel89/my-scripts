    echo "  2. You must now manually install the kernel/packages."
    echo ""
    echo -e "  Run the following commands as root:"
    echo -e "${GREEN}  apt update${NC}"
    echo -e "${GREEN}  apt install proxmox-ve postfix open-iscsi chrony${NC}"
    echo ""
